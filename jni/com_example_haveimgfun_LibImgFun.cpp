void im2double(Mat &image)
{
	Mat dimage(image.rows,image.cols,CV_32F);
	for (int i=0;i<image.cols;i++)
	{
		for (int j=0;j<image.rows;j++)
		{
			dimage.at<float>(j,i)=float(image.at<uchar>(j,i))/255;
		}
	}
	image=dimage;
}

void conv2d(Mat &image,Mat &result,int flag)
{
    
	Mat mask1=(Mat_<float>(3,3)<<-1,-1,-1,2, 2 ,2,-1, -1, -1);	
	Mat mask2=(Mat_<float>(3,3)<<1,2,1,  2,4,2,  1,2,1);	
	mask2/=16;
	Mat mask;
	if (flag==0)
	{
		mask=mask1.clone();
		Mat result1,result2,result3,result4,result5;
		filter2D (image,result1,image.depth (),mask);
		pow(result1, 2, result2);
		filter2D(result2,result3,image.depth(),mask);
		filter2D(result1,result4,image.depth(),mask);
		pow(result4, 2, result5);
		absdiff(result3, result5, result);
		//result=abs(result4-result5);
		sqrt(result,result);
		sqrt(result,result);
		threshold(result, result, 0.5, 1, CV_THRESH_BINARY);    
	}
	else
	{
		mask=mask2.clone();
		Mat result1,result2,result3,result4,result5;
		filter2D (image,result1,image.depth (),mask);
		pow(result1, 2, result2);
		filter2D(result2,result3,image.depth(),mask);
		filter2D(result1,result4,image.depth(),mask);
		pow(result4, 2, result5);
		absdiff(result3, result5, result);
		//result=abs(result4-result5);
		sqrt(result,result);
		sqrt(result,result);
	}
}


void im2uchar(Mat &image)
{
	Mat dimage(image.rows,image.cols,CV_8U);
	for (int i=0;i<image.cols;i++)
	{
		for (int j=0;j<image.rows;j++)
		{
			dimage.at<uchar>(j,i)=uchar(image.at<float>(j,i)*255);
		}
	}
	image=dimage;
}

void cvFillHoles(Mat &result)
{
	//assume input is uint8 B & W (0 or 1)
	//this function imitates imfill(image,'hole')
	Mat origin=result.clone();
	subtract(255,result,result);
	floodFill(result,Point2i(0,0),Scalar(1));
	floodFill(result,Point2i(result.cols-1,result.rows-1),Scalar(1));
	add(origin,result,result);
}


void cvFillHoles1(Mat &result)
{
	//assume input is uint8 B & W (0 or 1)
	//this function imitates imfill(image,'hole')
	Mat origin=result.clone();
	subtract(255,result,result);
	floodFill(result,Point2i((result.cols)/2-1,(result.rows)/2-1),Scalar(1));
	add(origin,result,result);
}


void removeSmallBlobs(cv::Mat& im, double size)
{
	// Only accept CV_8UC1
	if (im.channels() != 1 || im.type() != CV_8U)
		return;

	// Find all contours
	std::vector<std::vector<cv::Point> > contours;
	cv::findContours(im.clone(), contours, CV_RETR_EXTERNAL, CV_CHAIN_APPROX_SIMPLE);

	for (int i = 0; i < contours.size(); i++)
	{
		// Calculate contour area
		double area = cv::contourArea(contours[i]);

		// Remove small objects by drawing the contour with black color
		if (area > 0 && area <= size)
			cv::drawContours(im, contours, i, CV_RGB(0,0,0), -1);
	}
}


void startlocate(Mat &result,int &startline)
{
	//im2double(result);
	//result.convertTo(result,CV_32F);
	float* ysum=new float[result.cols];
	for(int jj=0;jj<result.cols;jj++){
		ysum[jj]=0;
	}
	
	for (int i=0;i<result.rows;i++)
	{
		for (int j=0;j<result.cols;j++)
		{
			ysum[j]=ysum[j]+float(result.at<uchar>(i,j));
		}
	}


    float maxsum=0;
	int yindex=0;
	for (int j=1;j<result.cols;j++)
	{
		if (ysum[j]>maxsum)
		{
			maxsum=ysum[j];
			yindex=j;
		}
	}
	delete[] ysum;
	startline=yindex;
	//cout<<startline<<endl;
}


void counting(Mat &image,int &locate,int &number,int lnum,int padding)
{
	//im2double(image);
	float* cnum=new float[lnum];
	for (int i=0;i<lnum;i++)
	{
		cnum[i]=0;
	}

	int* yy=new int[lnum];
	for (int i=0;i<=lnum-1;i++)
	{
		yy[i]=locate+(i-lnum/2)*padding;
		//cout<<yy[i]<<endl;
	}
	
	for (int a=0;a<lnum;a++) 
	{
		for (int b=0;b<image.rows-1;b++)
		{
			if(image.at<uchar>(b+1,yy[a])-image.at<uchar>(b,yy[a])>0)
			{
				cnum[a]=cnum[a]+1;
			}
		}
	}
	
	
	float cmean=0;
	for (int a=0;a<lnum;a++)
	{
		cmean=cmean+cnum[a]/lnum;
	}
	number=int(cmean+0.5);
	 delete[] cnum;
	 delete[] yy;
}

